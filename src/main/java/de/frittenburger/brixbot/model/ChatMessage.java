package de.frittenburger.brixbot.model;

public class ChatMessage {
	
	private long time;
	private String text;
	
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
	
}
